package com.btcetrade.bitcoin.model;

public class Data {

	private String network;
	private String address;
	private String confirmSentValue;
	private String unConfirmSentValue;
	public String getNetwork() {
		return network;
	}
	public void setNetwork(String network) {
		this.network = network;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	public String getConfirmSentValue() {
		return confirmSentValue;
	}
	public void setConfirmSentValue(String confirmSentValue) {
		this.confirmSentValue = confirmSentValue;
	}
	public String getUnConfirmSentValue() {
		return unConfirmSentValue;
	}
	public void setUnConfirmSentValue(String unConfirmSentValue) {
		this.unConfirmSentValue = unConfirmSentValue;
	}
	
	
	
}
